.MODEL	SMALL
.STACK	100H
.DATA		

MSG1 	DB  ' half-dollar $'
MSG2	DB	' quarter $'
MSG3	DB	' dime $'
MSG4	DB	' nickel $'
MSG5	DB	' penny $'

.CODE
MAIN	PROC
	MOV AX, @DATA
	MOV DS, AX

	CALL	INDEC	
	
	PUSH	AX		

	MOV	AH, 2
	MOV	DL, 0DH
	INT	21H
	MOV	DL, 0AH
	INT	21H
	
;HALF-DOLLAR
	XOR DX, DX
	POP	AX
	MOV	BX, 50D
	DIV	BX
	
	CALL	OUTDEC
	
	MOV AX, DX
	PUSH AX

	MOV AH, 9
	LEA	DX, MSG1
	INT 21H

	MOV AH, 2
	MOV	DL, 0DH
	INT	21H
	MOV	DL, 0AH
	INT	21H
	
;QUARTER
	XOR DX, DX
	
	POP	AX
	
	MOV	BX, 25D
	DIV	BX
	
	CALL	OUTDEC
	
	MOV AX, DX
	PUSH AX

	MOV AH, 9			
	LEA	DX, MSG2
	INT 21H

	MOV AH, 2
	MOV	DL, 0DH
	INT	21H
	MOV	DL, 0AH
	INT	21H

	XOR DX, DX
	POP	AX
	MOV	BX, 10D
	DIV	BX
	
	CALL	OUTDEC
	
	MOV AX, DX
	PUSH AX
	
;DIME
	MOV AH, 9			
	LEA	DX, MSG3
	INT 21H

	MOV AH, 2
	MOV	DL, 0DH
	INT	21H
	MOV	DL, 0AH
	INT	21H
	
;NICKEL
	XOR DX, DX			
	POP	AX
	MOV	BX, 5D
	DIV	BX
	
	CALL	OUTDEC
	
	MOV AX, DX
	PUSH AX

	MOV AH, 9
	LEA	DX, MSG4
	INT 21H

	MOV AH, 2
	MOV	DL, 0DH
	INT	21H
	MOV	DL, 0AH
	INT	21H
	
;PENNY
	POP	AX		
	
	CALL	OUTDEC

	MOV AH, 9
	LEA	DX, MSG5
	INT 21H

	MOV AH, 2
	MOV	DL, 0DH
	INT	21H
	MOV	DL, 0AH
	INT	21H

	MOV	AH, 4CH
	INT	21H
	
MAIN	ENDP
INCLUDE	OUTDEC.ASM	
INCLUDE	INDEC.ASM	
	END	MAIN